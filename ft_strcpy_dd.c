#include "libft.h"

void	ft_strcpy_dd(char **tab, char const *s, char c)
{
	int i;
	int j;
	int k;

	i = -1;
	k = 0;
	while (s[++i])
	{
		if (s[i] != c)
		{
			j = -1;
			while (s[i] != c && s[i])
				tab[k][++j] = s[i++];
			tab[k++][++j] = '\0';
		}
		if (!s[i])
			break ;
	}
}

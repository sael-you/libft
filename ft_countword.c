#include "libft.h"

int		ft_countword(char const *s, char c)
{
	int cpt;

	cpt = 0;
	while (*s)
	{
		while (*s == c)
			s++;
		if (*s)
		{
			while (*s && *s++ != c)
				;
			cpt++;
		}
	}
	return (cpt + 1);
}
